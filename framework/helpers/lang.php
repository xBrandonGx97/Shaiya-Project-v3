<?php
function lang($lang)
{
    switch ($lang) {
        case 'en':
            return 'English';
            break;
        case 'es':
            return 'Spanish';
            break;
        case 'pt':
            return 'Portugese';
            break;
        case 'fr':
            return 'French';
            break;
        case 'tr':
            return 'Turkish';
            break;
        case 'nl':
            return 'Dutch';
            break;
        case 'de':
            return 'German';
            break;
        case 'it':
            return 'Italian';
            break;
        case 'fil':
            return 'Filipino';
    }
}
